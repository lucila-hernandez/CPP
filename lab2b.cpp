// This program creates two integer variables and displays how many
// apples and oranges are stored in each variable.

#include <iostream>
using namespace std;

int main()
{
    // Variables of int data type 
    int Apples, Oranges;
    
    // Assign value to variables
    Apples = 10;
    Oranges = 20;

    // Display how many apples and oranges there are
    cout << "I have " << Apples << " Apples and " << Oranges << " Oranges.";

    return 0;
} 